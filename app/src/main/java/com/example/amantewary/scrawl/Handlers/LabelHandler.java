package com.example.amantewary.scrawl.Handlers;

public class LabelHandler {
    String name;
    Integer id;

    public LabelHandler(String name, Integer id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public Integer getId() {
        return id;
    }
}
